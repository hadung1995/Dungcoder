public class Hinhanh {
    private int id;
    private String hinhanh;

    public Hinhanh(int id, String hinhanh) {
        this.id = id;
        this.hinhanh = hinhanh;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHinhanh() {
        return hinhanh;
    }

    public void setHinhanh(String hinhanh) {
        this.hinhanh = hinhanh;
    }
}
